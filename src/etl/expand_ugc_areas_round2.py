def main():
    ap = argparse.ArgumentParser(description="UGC Round 2 扩展")
    ap.add_argument("--dry-run", action="store_true")
    ap.add_argument("--only-new", action="store_true",
                    help="只扩新片区，不深化老片区")
    ap.add_argument("--only-deepen", action="store_true")
    args = ap.parse_args()

    if args.dry_run:
        print("=== DRY RUN ===")
        print(f"\n新增 {len(AREAS_NEW)} 个片区：")
        for k, v in AREAS_NEW.items():
            print(f"  {k:<25} target={v['target_count']:>3}  text_len={len(v['raw_text']):>4}")
        print(f"\n深化 {len(AREAS_DEEPEN)} 个片区：")
        for k, v in AREAS_DEEPEN.items():
            print(f"  {k:<25} target={v['target_count']:>3}  text_len={len(v['raw_text']):>4}")
        total = sum(v["target_count"] for v in AREAS_NEW.values()) + \
                sum(v["target_count"] for v in AREAS_DEEPEN.values())
        print(f"\n预计总新增 aspect 数：{total}")
        return

    items = []
    if not args.only_deepen:
        items.extend(("NEW", k, v) for k, v in AREAS_NEW.items())
    if not args.only_new:
        items.extend(("DEEPEN", k, v) for k, v in AREAS_DEEPEN.items())

    print(f"=== Round 2 扩展开始 共 {len(items)} 项 ===")
    total = 0
    for i, (kind, k, v) in enumerate(items, 1):
        print(f"\n[{i}/{len(items)}] {kind} {k}")
        try:
            _, n = expand_area(
                area_anchor=k,
                raw_text=v["raw_text"],
                target_count=v["target_count"],
                source_urls=v["source_urls"],
            )
            total += n
        except Exception as e:
            print(f"  [ERROR] {type(e).__name__}: {e}")

    print(f"\n=== 完成 ===")
    print(f"新增：{total} 条")
    conn = get_conn()
    cur = conn.cursor()
    print(f"\n=== 当前 ugc_aspects 总览 ===")
    print(f"  TOTAL: {cur.execute('SELECT COUNT(*) FROM ugc_aspects').fetchone()[0]} 条")
    print(f"\n=== 片区分布 top 15 ===")
    for row in cur.execute(
        "SELECT area_anchor, COUNT(*) FROM ugc_aspects "
        "GROUP BY area_anchor ORDER BY 2 DESC LIMIT 15"
    ):
        print(f"  {row[0]:<28} {row[1]:>3} 条")
    conn.close()


if __name__ == "__main__":
    main()
