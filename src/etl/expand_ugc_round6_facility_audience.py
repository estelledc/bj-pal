def main():
    t0 = time.time()
    total_inserted = 0
    for area, cfg in AREAS.items():
        print(f"\n{'='*60}")
        print(f"扩展 {area} target={cfg['target_count']}")
        print('='*60)
        try:
            _, n = expand_area(
                area_anchor=area,
                raw_text=cfg["raw_text"],
                target_count=cfg["target_count"],
                source_urls=cfg["source_urls"],
            )
            total_inserted += n
        except Exception as e:
            print(f"  ERROR {area}: {type(e).__name__}: {e}")
    print(f"\n{'='*60}")
    print(f"完成：共扩 {total_inserted} 条 UGC，耗时 {time.time()-t0:.0f}s")
    print('='*60)


if __name__ == "__main__":
    main()
