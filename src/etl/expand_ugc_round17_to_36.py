def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--rounds", type=str, default=None)
    args = ap.parse_args()

    selected = list(ROUNDS.keys())
    if args.rounds:
        selected = [int(r) for r in args.rounds.split(",")]
        selected = [r for r in selected if r in ROUNDS]

    t0 = time.time()
    total_inserted = 0
    total_areas = sum(len(ROUNDS[r]) for r in selected)
    done_areas = 0

    for round_idx in selected:
        round_data = ROUNDS[round_idx]
        print(f"\n{'#'*65}")
        print(f"# Round {round_idx} ({len(round_data)} 子项)")
        print(f"{'#'*65}", flush=True)
        for area, cfg in round_data.items():
            done_areas += 1
            print(f"\n[{done_areas}/{total_areas}] {area} target={cfg['target_count']}", flush=True)
            try:
                _, n = expand_area(
                    area_anchor=area,
                    raw_text=cfg["raw_text"],
                    target_count=cfg["target_count"],
                    source_urls=cfg["source_urls"],
                    verbose=True,
                )
                total_inserted += n
            except Exception as e:
                print(f"  ERROR {area}: {type(e).__name__}: {e}", flush=True)

    elapsed = time.time() - t0
    print(f"\n{'#'*65}")
    print(f"# 完成: {len(selected)} 轮, {total_areas} 子项, {total_inserted} 条 UGC 入库")
    print(f"# 耗时 {elapsed:.0f}s ({elapsed/60:.1f} 分钟)")
    print(f"{'#'*65}", flush=True)


if __name__ == "__main__":
    main()
