def main():
    ap = argparse.ArgumentParser(description="UGC Class C 场景主题扩展")
    ap.add_argument("--dry-run", action="store_true")
    ap.add_argument("--scenarios", default="",
                    help="逗号分隔，限定要扩的场景")
    args = ap.parse_args()

    keys = list(SCENARIOS.keys())
    if args.scenarios:
        wanted = {x.strip() for x in args.scenarios.split(",")}
        keys = [k for k in keys
                if k in wanted or k.replace("scenario:", "") in wanted]

    if args.dry_run:
        print("=== Class C DRY RUN ===")
        for k in keys:
            v = SCENARIOS[k]
            print(f"  {k:<40} target={v['target_count']:>3}  text_len={len(v['raw_text']):>4}")
        total = sum(SCENARIOS[k]["target_count"] for k in keys)
        print(f"\n预计总新增 {total} 条")
        return

    print(f"=== Class C 场景扩展 共 {len(keys)} 项 ===")
    total = 0
    for i, k in enumerate(keys, 1):
        v = SCENARIOS[k]
        print(f"\n[{i}/{len(keys)}] {k}  ({v['title']})")
        try:
            _, n = expand_area(
                area_anchor=k,
                raw_text=v["raw_text"],
                target_count=v["target_count"],
                source_urls=v["source_urls"],
            )
            total += n
        except Exception as e:
            print(f"  [ERROR] {type(e).__name__}: {e}")

    print(f"\n=== Class C 完成 ===")
    print(f"新增：{total} 条")


if __name__ == "__main__":
    main()
