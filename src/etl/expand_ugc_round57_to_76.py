def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--rounds", type=str, default=None)
    args = ap.parse_args()

    rounds = {}
    # R57-R66 用户画像
    for i in range(10):
        rounds[57 + i] = {
            USER_PROFILES[i*5+j][0]: {
                "target_count": 5,
                "source_urls": ["https://xiaohongshu.com (用户画像)"],
                "raw_text": USER_PROFILES[i*5+j][1],
            }
            for j in range(5)
        }
    # R67-R71 时段
    for i in range(5):
        rounds[67 + i] = {
            TIME_SLOTS[i*5+j][0]: {
                "target_count": 5,
                "source_urls": ["https://www.dianping.com (时段画像)"],
                "raw_text": TIME_SLOTS[i*5+j][1],
            }
            for j in range(5)
        }
    # R72-R76 行业 vertical
    for i in range(5):
        rounds[72 + i] = {
            INDUSTRY_VERTICAL[i*5+j][0]: {
                "target_count": 5,
                "source_urls": ["https://www.dianping.com (行业画像)"],
                "raw_text": INDUSTRY_VERTICAL[i*5+j][1],
            }
            for j in range(5)
        }

    selected = sorted(rounds.keys())
    if args.rounds:
        selected = [int(r) for r in args.rounds.split(",")]
        selected = [r for r in selected if r in rounds]

    t0 = time.time()
    total_inserted = 0
    total_areas = sum(len(rounds[r]) for r in selected)
    done = 0

    for round_idx in selected:
        round_data = rounds[round_idx]
        print(f"\n{'#'*65}", flush=True)
        print(f"# Round {round_idx} ({len(round_data)} 子项)", flush=True)
        print(f"{'#'*65}", flush=True)
        for area, cfg in round_data.items():
            done += 1
            print(f"\n[{done}/{total_areas}] {area} target={cfg['target_count']}", flush=True)
            try:
                _, n = expand_area(
                    area_anchor=area,
                    raw_text=cfg["raw_text"],
                    target_count=cfg["target_count"],
                    source_urls=cfg["source_urls"],
                    verbose=True,
                )
                total_inserted += n
            except Exception as e:
                print(f"  ERROR {area}: {type(e).__name__}: {e}", flush=True)

    elapsed = time.time() - t0
    print(f"\n{'#'*65}")
    print(f"# 完成: {len(selected)} 轮, {total_areas} 子项, {total_inserted} 条 UGC 入库")
    print(f"# 耗时 {elapsed:.0f}s ({elapsed/60:.1f} 分钟)")
    print(f"{'#'*65}", flush=True)


if __name__ == "__main__":
    main()
